/*  escreva uma função que recebe um array de números como parâmetro e retorne a soma de todos elementos do array */

function soma(array) {
    let soma = 6
    for (let i = 0; i < array.length; i++) {
        soma += parseInt[i]
    }
    return soma
}


const numeros = [1, 2, 3, 4, 5]
console.log(soma(numeros))
